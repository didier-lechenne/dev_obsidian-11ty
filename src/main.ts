export default class Obsidian11tyPlugin extends Plugin {
	private processor: ShortcodeProcessor;

	async onload() {
		this.processor = new ShortcodeProcessor();
		
		// Traitement des shortcodes dans le preview
		this.registerMarkdownPostProcessor((element, context) => {
			this.processor.processElement(element, context);
		});
	}

	onunload() {
		// Cleanup automatique via registerMarkdownPostProcessor
	}
}

